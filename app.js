  getByCategory(cat) {
    if (cat === 'all') return this.items;
    return this.items.filter(i => i.category === cat);
  },
  search(query) {
    const q = query.toLowerCase();
    return this.items.filter(i =>
      i.title.toLowerCase().includes(q) ||
      i.short.toLowerCase().includes(q) ||
      (i.tags && i.tags.some(t => t.toLowerCase().includes(q)))
    );
  },
  getById(id) { return this.items.find(i => i.id === id); },
  getCategoryLabel(cat) {
    const m = { technique: 'Техника', mistake: 'Ошибка', biblical: 'Духовный подход', theory: 'Теория' };
    return m[cat] || cat;
  }
