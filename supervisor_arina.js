/* ════════════════════════════════════════════════════════════════════
   ПОСЛЕСЕССИОННЫЙ ЧАТ С АРИНОЙ
   Модуль живёт отдельно от сессии — у него своя память контекста
════════════════════════════════════════════════════════════════════ */
const ArinaChat = {

  _messages: [],        // история чата
  _sessionData: null,   // данные завершённой сессии
  _transcript: '',      // транскрипт для контекста

  /* Инициализация после сессии */
  init(sessionResult, transcript) {
    this._messages   = [];
    this._sessionData = sessionResult;
    this._transcript  = transcript || '';

    const container = document.getElementById('arina-chat-messages');
    if (container) container.innerHTML = '';

    // Арина сама начинает разговор
    this._addArinaOpening(sessionResult);
  },

  /* Арина открывает диалог сама — не ждёт вопроса */
  async _addArinaOpening(result) {
    const score = result.score || 65;
    const a     = result.analysis || {};

    // Генерируем живое открытие через AI
    try {
      const opening = await Engine.chat(
        ARINA_SYSTEM,
        [{
          role: 'user',
          content: `Студент только что завершил учебную сессию. Твоя задача — начать живой разговор, НЕ пересказывать отчёт.

Данные сессии:
- Клиент: ${result.clientName}, запрос: "${result.clientRequest || ''}"
- Оценка: ${score}/100
- Реплик студента: ${result.msgs}
- Длительность: ${result.duration} мин
- Главное что было хорошо: ${a.worked || 'в анализе'}
- Главная точка роста: ${a.missed || 'в анализе'}
- Ключевые ошибки: ${a.errors || 'не выявлено'}

Напиши живое открывающее сообщение (3-4 предложения):
- Конкретно назови одну вещь которая зацепила тебя в сессии
- Задай ОДИН открытый вопрос который побудит студента рефлексировать
- Не пересказывай оценки из отчёта — скажи что-то новое
- Говори как живой человек, не как система оценки`
        }],
        { model: Engine.MODELS.supervisor, maxTokens: 200 }
      );
      this._addMessage('arina', opening.trim());
    } catch(e) {
      // Fallback — статичное открытие
      const fallback = score >= 70
        ? `Ладно, давай поговорим. Отчёт отчётом, но меня больше интересует другое — что вы сами почувствовали в этой сессии? Был ли момент когда вы не знали что делать?`
        : `Посмотрела на сессию. Там есть над чем поработать — но это нормально, для этого мы здесь. Скажите мне вот что: был момент в этом разговоре когда вы почувствовали что потеряли нить? Что происходило тогда?`;
      this._addMessage('arina', fallback);
    }
  },

  /* Студент задаёт вопрос */
  async send() {
    const input = document.getElementById('arina-chat-input');
    const text  = (input?.value || '').trim();
    if (!text) return;
    input.value = '';

    this._addMessage('student', text);
    this._messages.push({ role: 'user', content: text });

    this._showTyping();

    try {
      const systemContext = this._buildContext();
      const response = await Engine.chat(
        systemContext,
        this._messages,
        { model: Engine.MODELS.supervisor, maxTokens: 400 }
      );

      this._hideTyping();
      this._addMessage('arina', response.trim());
      this._messages.push({ role: 'assistant', content: response.trim() });

    } catch(e) {
      this._hideTyping();
      this._addMessage('arina', 'Что-то пошло не так с соединением. Попробуйте ещё раз.');
    }
  },

  /* Системный контекст для чата — Арина помнит всю сессию */
  _buildContext() {
    const a = this._sessionData?.analysis || {};
    const r = this._sessionData || {};

    return `${ARINA_SYSTEM}

КОНТЕКСТ РАЗБИРАЕМОЙ СЕССИИ:
Клиент: ${r.clientName || '?'}, категория: ${r.clientType || '?'}
Запрос клиента: "${r.clientRequest || ''}"
Оценка сессии: ${r.score || '?'}/100
Реплик студента: ${r.msgs || '?'}, длительность: ${r.duration || '?'} мин

АНАЛИЗ СЕССИИ (твой предварительный разбор):
- Что сработало: ${a.worked || '—'}
- Что улучшить: ${a.missed || '—'}
- Ошибки: ${a.errors || 'не выявлено'}
- Рекомендации: ${a.recommendations || '—'}

${this._transcript ? `ФРАГМЕНТ ТРАНСКРИПТА (последние реплики):\n${this._transcript.slice(-1500)}` : ''}

ТВОЯ ЗАДАЧА В ЭТОМ ЧАТЕ:
Ты ведёшь живой диалог-разбор с студентом. Отвечай развёрнуто но конкретно (3-6 предложений). 
Если студент спрашивает о технике — объясни и покажи как применить именно к этому клиенту.
Если студент защищается ("я и так всё правильно делал") — не спорь, задай вопрос который заставит задуматься.
Если студент спрашивает про смысл, духовное, религиозное — используй Франкла.
Если вопрос про зависимость — апеллируй к МИ.
Если вопрос про травму — напомни про окно толерантности и темп.
Ссылайся на конкретные моменты транскрипта когда это уместно.`;
  },

  /* Быстрые вопросы — кнопки для студента */
  quickQuestion(q) {
    const input = document.getElementById('arina-chat-input');
    if (input) {
      input.value = q;
      input.focus();
    }
  },

  /* ── UI ── */
  _addMessage(role, text) {
    const container = document.getElementById('arina-chat-messages');
    if (!container) return;

    const div = document.createElement('div');
    div.className = `arina-msg arina-msg--${role}`;

    if (role === 'arina') {
      div.innerHTML = `
        <div class="arina-msg__avatar">А</div>
        <div class="arina-msg__bubble">${UI.renderMarkdown ? UI.renderMarkdown(text) : text}</div>
      `;
    } else {
      div.innerHTML = `
        <div class="arina-msg__bubble arina-msg__bubble--student">${UI.escapeHtml(text)}</div>
        <div class="arina-msg__avatar arina-msg__avatar--student">Вы</div>
      `;
    }

    container.appendChild(div);
    container.scrollTop = container.scrollHeight;
  },

  _showTyping() {
    const container = document.getElementById('arina-chat-messages');
    if (!container) return;
    const div = document.createElement('div');
    div.className = 'arina-msg arina-msg--arina';
    div.id = 'arina-typing';
    div.innerHTML = `
      <div class="arina-msg__avatar">А</div>
      <div class="arina-msg__bubble arina-typing-indicator">
        <span></span><span></span><span></span>
      </div>
    `;
    container.appendChild(div);
    container.scrollTop = container.scrollHeight;
  },

  _hideTyping() {
    document.getElementById('arina-typing')?.remove();
  },
};


